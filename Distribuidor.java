    private void listarDistribuidor() {
        String sql = "select * from DISTRIBUIDOR"; //listar todos los libros
        try {
            cn = con.getConnection();
            st =  cn.createStatement(); //crea la consulta
            rs = st.executeQuery(sql); //resultado consulta
            
            Object[] DISTRIBUIDOR = new Object[5];
            modelo = (DefaultTableModel) tablaDistribuidor.getModel();
            while (rs.next()){
             DISTRIBUIDOR[0] = rs.getInt("rut_distribuidor");
             DISTRIBUIDOR[1] = rs.getString("nombre");
             DISTRIBUIDOR[2] = rs.getString("calle");
             DISTRIBUIDOR[3] = rs.getInt("numero");
             DISTRIBUIDOR[4] = rs.getString("telefono");
             modelo.addRow(DISTRIBUIDOR); //agregamos las filas 
            }
            
        
        }catch(Exception e){
        
        }
        tablaDistribuidor.setModel(modelo); //se muestra la tabla
        
    }
    
     public void remueveFila(){
    DefaultTableModel tb = (DefaultTableModel) tablaDistribuidor.getModel();
    int a = tablaDistribuidor.getRowCount() - 1;
    for(int i=a; i>=0; i--){
      tb.removeRow(tb.getRowCount()- 1);
    }
}

    private void agregarDistribuidor() {
        Connection conexion = null;
        try{
           conexion = con.getConnection();
           ps = conexion.prepareStatement("insert into DISTRIBUIDOR(rut_distribuidor,nombre,calle,numero,telefono)values(?,?,?,?,?)");
           ps.setInt(1,Integer.parseInt(txtRut.getText()));
           ps.setString(2,txtNombre.getText());
           ps.setString(3,txtCalle.getText());
           ps.setInt(4,Integer.parseInt(txtNumero.getText()));
           ps.setString(5,txtTelefono.getText());
           
           int res = ps.executeUpdate();
           if(res > 0){
             JOptionPane.showMessageDialog(null, "Distribuidor ha sido ingresado");
             remueveFila();
             listarDistribuidor();
           
           }else{
            JOptionPane.showMessageDialog(null, "Distribuidor no ha sido ingresado");
           
           }
           conexion.close();    
        
        }
        catch(Exception e){
            System.err.print(e);
        
        }
    }
     
