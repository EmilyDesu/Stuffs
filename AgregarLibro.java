    //metodo listar 
    private void listar() {

        String sql = "select * from LIBRO"; //listar todos los libros
        try {
            cn = con.getConnection();
            st = cn.createStatement(); //crea la consulta
            rs = st.executeQuery(sql); //resultado consulta

            Object[] LIBRO = new Object[5];
            modelo = (DefaultTableModel) TablaDatos.getModel();
            while (rs.next()) {
                LIBRO[0] = rs.getInt("serie");
                LIBRO[1] = rs.getString("ISBN");
                LIBRO[2] = rs.getString("titulo");
                LIBRO[3] = rs.getInt("cant_paginas");
                LIBRO[4] = rs.getInt("precio");
                modelo.addRow(LIBRO); //agregamos las filas 
            }

        } catch (Exception e) {

        }
        TablaDatos.setModel(modelo); //se muestra la tabla

    }

    private void agregarLibro() {
        Connection conexion = null;
        try {
            conexion = con.getConnection();
            ps = conexion.prepareStatement("insert into LIBRO(ISBN,titulo,cant_paginas,precio)values(?,?,?,?)");
            ps.setString(1, txtIsbn.getText()); //capturo ISBN como string
            ps.setString(2, txtTitulo.getText());//capturo titulo
            ps.setInt(3, Integer.parseInt(txtPaginas.getText()));//capturo cant pagina como entero
            ps.setInt(4, Integer.parseInt(txtPrecio.getText()));

            int res = ps.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Libro Ingresado");
                remueveFila();
                listar(); //listar 
            } else {
                JOptionPane.showMessageDialog(null, "Error, Libro no Ingresado");
            }
            conexion.close();
        } catch (Exception e) {
            System.err.print(e);

        }
    }

    public void remueveFila() {
        DefaultTableModel tb = (DefaultTableModel) TablaDatos.getModel();
        int a = TablaDatos.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }

    }

