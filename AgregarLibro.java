    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        int fila_seleccionada = TablaDatos.getSelectedRow(); //selecciona la fila
        if (fila_seleccionada == -1) {
            JOptionPane.showMessageDialog(null, "Usuario no seleccionado");
        } else {
            txtSerie.setText(TablaDatos.getValueAt(fila_seleccionada,0).toString());
            txtIsbn.setText(TablaDatos.getValueAt(fila_seleccionada,1).toString());
            txtTitulo.setText(TablaDatos.getValueAt(fila_seleccionada,2).toString());
            txtPaginas.setText(TablaDatos.getValueAt(fila_seleccionada,3).toString());
            txtPrecio.setText(TablaDatos.getValueAt(fila_seleccionada,4).toString());
            filas=fila_seleccionada;
        }

    }//GEN-LAST:event_TablaDatosMouseClicked
    int filas;
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String []datos= new String[5];
        datos[0]=txtSerie.getText();
        datos[1]=txtIsbn.getText();
        datos[2]=txtTitulo.getText();
        datos[3]=txtPaginas.getText();
        datos[4]=txtPrecio.getText();
        
        for (int i = 0; i < TablaDatos.getColumnCount(); i++) {
            modelo.setValueAt(datos[i], filas, i);
        } 
        
    }//GEN-LAST:event_btnModificarActionPerformed

