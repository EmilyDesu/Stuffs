    public void listarCompra() {
         String sql = "select * from COMPRA"; //listar todas las compras
        try {
            cn = con.getConnection();
            st = cn.createStatement(); //crea la consulta
            rs = st.executeQuery(sql); //resultado consulta

            Object[] COMPRA = new Object[3];
            modelo = (DefaultTableModel) TablaCompra.getModel();
            while (rs.next()) {
                COMPRA[0] = rs.getInt("LIBRO_serie");
                COMPRA[1] = rs.getInt("DISTRIBUIDOR_rut_distribuidor");
                COMPRA[2] = rs.getString("fecha"); //listar datetime
               // COMPRA[3] = rs.getString("fecha"); //listar datetime
                
                modelo.addRow(COMPRA); //agregamos las filas 
            }

        } catch (Exception e) {

        }
        TablaCompra.setModel(modelo); //se muestra la tabla
        
    }

    private void agregarCompra() {
        Connection conexion = null;
        try{
           conexion = con.getConnection();
           ps = conexion.prepareStatement("insert into COMPRA(LIBRO_serie,DISTRIBUIDOR_rut_distribuidor,fecha)values(?,?,?)");
           ps.setInt(0,Integer.parseInt(txtSeriec.getText()));
           ps.setString(1,txtRutD.getText());
           ps.setString(2,txtFecha.getText());
           
           int res = ps.executeUpdate();
           if(res > 0){
             JOptionPane.showMessageDialog(null, "Compra Realizada");
             remueveFila();
             listarCompra();
           
           }else{
            JOptionPane.showMessageDialog(null, "Datos de compra no ingresados");
           
           }
           conexion.close();    
        
        }
        catch(Exception e){
            System.err.print(e);
        
        }
    }

    private void remueveFila() {
        DefaultTableModel tb = (DefaultTableModel) TablaCompra.getModel();
    int a = TablaCompra.getRowCount() - 1;
    for(int i=a; i>=0; i--){
      tb.removeRow(tb.getRowCount()- 1);
    }
}
